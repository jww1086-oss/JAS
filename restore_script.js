const fs = require('fs');

const targetFile = 'app.js';
let content = fs.readFileSync(targetFile, 'utf8');

// 1. GAS_URL 중복 경로 수정
content = content.replace(/\/exec\/exec/g, "/exec");

// 2. 한글 깨짐 패턴 복구 매핑 (단순 문자열 치환 방식)
const mapping = [
    ["?시?", "실시간"],
    ["?트?크", "네트워크"],
    ["?태", "상태"],
    ["?데?트", "업데이트"],
    ["?수", "함수"],
    ["?시?ON", "실시간 ON"],
    ["?속 ?", "접속 중"],
    ["?이??보안", "데이터 보안"],
    ["?회", "우회"],
    ["??제", " 및 정제"],
    ["?틸리티", "유틸리티"],
    ["?업?", "작업명"],
    ["부?명", "부서명"],
    ["?시", "일시"],
    ["????", "점검자"],
    ["?업?계", "작업단계"],
    ["?험?인", "위험요인"],
    ["?재?전조치", "현재안전조치"],
    ["개선?책", "개선대책"],
    ["?재_?험??", "현재_위험도"],
    ["?류_?험??", "잔류_위험도"],
    ["?름", "이름"],
    ["?속", "소속"],
    ["?계", "단계"],
    ["?력", "입력"],
    ["?인", "확인"],
    ["?택", "선택"],
    ["?행", "실행"],
    ["?동", "자동"],
    ["?면", "화면"],
    ["?보", "정보"],
    ["?롭?운", "드롭다운"],
    ["?정", "설정"],
    ["?리", "처리"],
    ["?공", "제공"],
    ["?성", "생성"],
    ["?비", "준비"],
    ["?시", "표시"],
    ["?환", "전환"],
    ["?동", "이동"],
    ["?고", "종료"],
    ["?작", "시작"],
    ["?용", "사용"],
    ["?이??", "데이터"],
    ["?서", "문서"]
];

for (const [from, to] of mapping) {
    content = content.split(from).join(to);
}

// 3. 특정 정규식 패턴 복구 (Line 52, 53) - 문자열 기반 안전 치환
const regex1_old = '(?=[0-9]+\\.|[0-9]+\\)|[????|\\([0-9]+\\)|(?:\\n|^)[-*??)';
const regex1_new = '(?=[0-9]+\\.|[0-9]+\\)|[Ⅰ-Ⅳ]|\\([0-9]+\\)|(?:\\n|^)[-*●])';
content = content.split(regex1_old).join(regex1_new);

const regex2_old = '^[0-9]+\\.|^[0-9]+\\)|^[????|^\\([0-9]+\\)|^-|^\\*|^\\??^\\??';
const regex2_new = '^[0-9]+\\.|^[0-9]+\\)|^[Ⅰ-Ⅳ]|^\\([0-9]+\\)|^-|^\\*|^●|^ㆍ';
content = content.split(regex2_old).join(regex2_new);

fs.writeFileSync(targetFile, content, 'utf8');
console.log('Restoration Complete.');
